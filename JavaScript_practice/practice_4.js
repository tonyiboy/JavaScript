let day = "monday"

switch(day) {
  case "monday":
    console.log("7am");
    break;
  case "tuesday":
    console.log("8am");
    break;
  case "wednesday":
    console.log("9am");
    break;
  case "thursday":
    console.log("6am");
    break;
  case "friday":
    console.log("7am");
    break;
  default:
    console.log("rest day");
}